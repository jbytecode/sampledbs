# Pi
pi = 3.14159265

print("The value of pi is {}".format(pi))

# The output is as follows:
# The value of pi is 3.14159265




# Another use of formatted strings
print(f"The value of pi is {pi}")

# The output is as follows:
# The value of pi is 3.14159265




